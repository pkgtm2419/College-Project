<p><b>Language in which to answer NEET – UG </b></p>
<p>As of now it is English / Hindi. Conducting NEET in regional languages is under debate.</p>

<p><b>Application fee for NEET – UG </b></p>
<p>Fee for NEET is Rs.1000/- for general category candidates and Rs.800/- for reservation candidates.</p>
            
			
			
            
