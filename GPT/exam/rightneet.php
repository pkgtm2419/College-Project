			<p><b>What is NEET – UG? </b></p>
<p>National Eligibility cum Entrance Test for undergraduate courses (NEET-UG) is the National level common entrance test conducted by CBSE for admissions to 335 medical colleges affiliated to Medical Council of India (MCI). MBBS and BDS seats are to be allotted on the basis of NEET – UG score of candidates.</p>

<p><b>Who conducts NEET? </b></p>
<p>The Central Board of Secondary Education (CBSE) on behalf of Medical Council of India (MCI).</p>

<p><b>Eligibility for NEET UG </b></p>
<p>Pass in 10 + 2 with a score of 50% (General category) and 40% (SC/ST/OBC) from a recognized board.</p>

<p><b>Age Eligibility for NEET – UG </b></p>
<p>A minimum of 17 years and maximum 25 years. Relaxation of upper age limit by 5 years for SC/ST/OBC candidates.</p>

<p><b>Reservation / Allotment of seats </b></p>
<p>SC/ST/OBC and differently abled candidates are eligible for reservation as per Govt. of India and the respective State norms. 85% of the medical college seats to be filled in by the State Merit list candidates and the remaining 15% seats by All India Merit list candidates.</p>

<p><b>Exam pattern for NEET – UG </b></p>
<p>NEET – UG test consists of 180 MCQs to be answered in 3 hours.</p>

<p><b>Date of first NEET – UG </b></p>
<p>Scheduled to be conducted on 5th May 2013.</p>

<p><b>Syllabus of NEET – UG </b></p>
<p>11th and 12th class syllabus of Physics, Chemistry, Biology (Botany & Zoology). Questions on the basis of the syllabus of these subjects.</p>
